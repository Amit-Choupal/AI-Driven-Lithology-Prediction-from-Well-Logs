# Lithology prediction model using geophysical well log data from North Sea

import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
from sklearn.model_selection import train_test_split
from sklearn.ensemble import RandomForestClassifier
from sklearn.impute import SimpleImputer
from sklearn.metrics import classification_report, f1_score, accuracy_score, confusion_matrix, ConfusionMatrixDisplay

# 1. Load and prep data
df = pd.read_csv('North_Sea_WellLog.csv')

# Splitting the feature and target
target = 'FORCE_2020_LITHOFACIES_LITHOLOGY'
features = ['GR', 'RHOB', 'NPHI', 'PEF', 'DTC', 'SP', 'RMED']

# Converting numeric codes into readable lithology names
lithology_key = {
    30000: 'Sandstone',
    65030: 'Sandstone/Shale',
    65000: 'Shale',
    80000: 'Marl',
    74000: 'Dolomite',
    70000: 'Limestone',
    70032: 'Chalk',
    88000: 'Halite',
    86000: 'Anhydrite',
    99000: 'Tuff',
    90000: 'Coal',
    93000: 'Basement'
}

# Drop rows where target is missing BEFORE creating your features/target sets
df = df.dropna(subset=[target])

df['Lithology_Name'] = df[target].map(lithology_key)
print("Preview of original dataset mapping:")
print(df[[target, 'Lithology_Name']].head())

X = df[features].copy()
y = df[target]

# Apply log transform to resistivity
if 'RMED' in X.columns:
    X['RMED'] = np.log10(X['RMED'] + 0.1)

# Impute missing values
imputer = SimpleImputer(strategy='median')
X_imputed = imputer.fit_transform(X)

# 2. Train-Test Split
X_train, X_val, y_train, y_val = train_test_split(
    X_imputed, y, test_size=0.2, random_state=42, stratify=y
)

# 3. Train Random Forest with balanced class weights
model = RandomForestClassifier(
    n_estimators=100,
    class_weight='balanced',
    random_state=42,
    n_jobs=-1,
    max_depth=20
)

model.fit(X_train, y_train)

# 4. Evaluate the model
y_pred = model.predict(X_val)
acc = accuracy_score(y_val, y_pred)
print(f"\nOverall Validation Accuracy Score: {acc:.4f}")

# Generate the raw confusion matrix array
cm = confusion_matrix(y_val, y_pred)

fig, ax = plt.subplots(figsize=(10, 10))
display_names = [lithology_key[code] for code in model.classes_]

disp = ConfusionMatrixDisplay(confusion_matrix=cm, display_labels=display_names)
disp.plot(cmap='Blues', ax=ax, xticks_rotation=45)
plt.title("Lithology Prediction Confusion Matrix")
plt.tight_layout()

plt.savefig('lithology_confusion_matrix.png', dpi=300, bbox_inches='tight')
plt.show()

# Features influencing the lithology the most

active_features = ['GR', 'RHOB', 'NPHI', 'PEF', 'DTC', 'RMED']

feature_importances = pd.Series(model.feature_importances_, index=active_features)
feature_importances.sort_values(ascending=False).plot(kind='bar')

# 6. Making Prediction on new Hypothetical data
print("\n--- Making Prediction on New Hypothetical Sample ---")

# Ensure columns match the exact sequence of your 'features' list
New_Sample = pd.DataFrame({
    'GR': [28],
    'RHOB': [2],
    'NPHI': [0.3],
    'PEF': [np.nan],
    'DTC': [154],
    'SP': [np.nan],
    'RMED': [200]
})


New_Sample['RMED'] = np.log10(New_Sample['RMED'] + 0.1)
New_Sample_imputed = imputer.transform(New_Sample)

predicted_code = model.predict(New_Sample_imputed)[0]
predicted_name = lithology_key[predicted_code]

print(f"Predicted Numerical Code: {predicted_code}")
print(f"Predicted Lithology Name: {predicted_name}")